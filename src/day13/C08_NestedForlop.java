package day13;

public class C08_NestedForlop {
    public static void main(String[] args) {



        int boy=5;
        int en=5;
        for (int i = 1; i <=boy ; i++) {
            for (int j = 1; j <=en ; j++) {
                System.out.print("*  ");
            }
            System.out.println("");
        }


    }

}
