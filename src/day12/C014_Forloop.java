package day12;

public class C014_Forloop {

    public static void main(String[] args) {
        // 1 den 30 a kadar sayılrı virgül koyark yazdırın//
        int sayı = 10;
        int sayı1 = 30;
        for (int i = sayı; i <= sayı1; i++) {

            if (sayı <= sayı1) {
                System.out.print(i+",");
            }
        }


    }


}






